def maps_in_columns(images: list[Path], columns: int, title: str, top_text: str | None = None, bottom_text: str | None = None, bottom_text_vspace: str = "3em", bottom_text_font_size: list[float] = [7.0, 7.0]) -> list[str]:
    return [
        r"\newpage",
        fr"\subsection{{{title}}}" if title else "",
        r"\noindent " + top_text if top_text else "",
        *_maps_in_columns(images=images, columns=columns, newpage=False),
        *([rf"\vspace*{{{bottom_text_vspace}}}", rf"\noindent {{\fontsize{{{bottom_text_font_size[0]}}}{{{bottom_text_font_size[1]}}} \selectfont \textbf{{{bottom_text}}} }}"] if bottom_text else []),
    ]

def _maps_in_columns(images: list[Path], columns: int, newpage: bool) -> list[str]:

    setup: list[dict[str, str | int]] = [
        {},                     # columns: 0
        {},                     # columns: 1
        {                       # columns: 2
            "images_per_page": 6,
            "tex_begin": r"\begin{AntigenicMapTableWithSep}{7.0pt}{3.5}{9 / 30}",  # sep=7.5 arraystretch=3.5 scale=9/30
            "tex_end": r"\end{AntigenicMapTableWithSep}",
        },
        {                       # columns: 3
            "images_per_page": 6,
            "tex_begin": r"\begin{AntigenicMapTable3WithSep}{7.0pt}{3.5}{14 / 30}",
            "tex_end": r"\end{AntigenicMapTable3WithSep}",
        },
    ]

    def make_map(img: Path | None) -> str:
        if img:
            if img.exists():
                return fr"\AntigenicMap{{{img.resolve()}}}"
            else:
                return fr"{{\fontsize{{12}}{{16}} \selectfont \noindent \rotatebox{{45}}{{ \textbf{{ \textcolor{{red}}{{{img}}} }} }}}}"
        else:
            return fr"\hspace{{18em}} {{\fontsize{{12}}{{16}} \selectfont \noindent \rotatebox{{45}}{{ \textbf{{ \textcolor{{red}}{{ }} }} }}}}"

    def n_maps(imgs: list[Path | None]) -> str:
        return " & ".join(make_map(img) for img in imgs) + r"\\"

    result = []
    for page_no, images_on_page in enumerate(itertools.zip_longest(*([iter(images)] * setup[columns]["images_per_page"]))):
        result.extend([
            r"\newpage" if newpage or page_no > 0 else "",
            setup[columns]["tex_begin"],
            *[n_maps(images_on_page[no:no + columns]) for no in range(0, len(images_on_page), columns)],
            setup[columns]["tex_end"],
            ])
    return result

