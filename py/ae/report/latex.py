def _blank_page() -> list[str]:
    # http://tex.stackexchange.com/questions/36880/insert-a-blank-page-after-current-page
    return [r"\newcommand\blankpage{ \newpage \vspace*{100pt} \thispagestyle{empty} \newpage }"]

def _remove_section_numbering() -> list[str]:
    # http://www.ehow.com/how_8085363_hide-section-numbers-latex.html
    return [r"\setcounter{secnumdepth}{-1}"]

def _table_of_contents() -> list[str]:
    # http://tex.stackexchange.com/questions/163986/format-table-of-contents-with-latex
    return [
        r"\titlecontents{section}[0cm]{\bfseries}{\\}{\\}{}",
        r"\titlecontents{subsection}[1em]{}{}{}{\titlerule*[5pc]{}\vspace{0.8ex}\thecontentspage}",
        r"\contentsmargin{120pt}",
        r"\cftsetindents{section}{0.5in}{0.5in}",  # http://tex.stackexchange.com/questions/50471/question-about-indent-lengths-in-toc
    ]

def _prepare_geographic_coloring(source: list[dict[str, str]]) -> list[str]:
    def color_to_latex(color: str) -> str:
        if color and color[0] == "#":
            return color[1:]
        elif color == "transparent":
            return "FFFFFF"
        else:
            return color
    color_def = [fr"\definecolor{{{en['label']}}}{{HTML}}{{{color_to_latex(en['fill'])}}}" for en in source if en.get("label")]
    rows = [fr"\cbox[{en['label']}] & {en['label']} \\" for en in source if en.get("label")]
    return [
        *color_def,
        r"\begin{tabular}{l l}",
        *rows,
        r"\end{tabular}",
    ]

