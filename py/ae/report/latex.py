def geographic(subtype: str, title: str, coloring: list[dict[str, object]], time_series: TimeSeriesRange, desc: str) -> list[str]:
    images = [Path("geo", f"{subtype}-{mstart.strftime('%Y-%m')}.pdf").resolve() for mstart in time_series.range_begin()]
    images_per_page = 3
    groups = "\n\\newpage\n".join(r"\begin{GeographicMapsTable} " + " ".join(f"\\GeographicMap{{{img}}} \\\\\n" for img in images[i:i+images_per_page]) + r"\end{GeographicMapsTable}" for i in range(0, len(images), images_per_page))
    return [
        fr"\subsection{{{title} geographic data}}",
        r"\vspace*{3em}",
        f"Month-by-month geographic time series from {time_series.front().strftime('%B %Y')} to {time_series.back().strftime('%B %Y')}",
        r"\vspace*{1em}",
        r"\noindent",
        *_prepare_geographic_coloring(coloring),
        r"\vspace*{1em}",
        desc,
        r"\newpage",
        groups
    ]

