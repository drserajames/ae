sSubtypeForStat = {"h1": "A(H1N1)", "h3": "A(H3N2)", "b": "B", "bvic": "BVICTORIA", "byam": "BYAMAGATA"}

sContinents = ['ASIA', 'AUSTRALIA-OCEANIA', 'NORTH-AMERICA', 'EUROPE', 'RUSSIA', 'AFRICA', 'MIDDLE-EAST', 'SOUTH-AMERICA', 'CENTRAL-AMERICA', 'all', 'sera', 'sera_unique']

sHeader = {'ASIA': 'Asia', 'AUSTRALIA-OCEANIA': 'Oceania', 'NORTH-AMERICA': 'N Amer', 'EUROPE': 'Europe', 'RUSSIA': 'Russia', 'AFRICA': 'Africa',
           'MIDDLE-EAST': 'M East', 'SOUTH-AMERICA': 'S Amer', 'CENTRAL-AMERICA': 'C Amer', 'all': 'TOTAL', 'month': 'Year-Mo', 'year': 'Year',
           'sera': 'Sera', 'sera_unique': 'Sr Uniq'}

sReYearMonth = {'month': re.compile(r'^\d{6}$', re.I), 'year': re.compile(r'^\d{4}$', re.I)}

def _statistics_table(subtype: str, lab: str, time_series: TimeSeriesRange, current_stat: Path, previous_stat: Path | None) -> list[str]:

    data = json.load(lzma.LZMAFile(current_stat, "rb"))
    data_antigens = data['antigens'][sSubtypeForStat[subtype]].get(lab_title(lab), {})
    data_sera_unique = data['sera_unique'].get(sSubtypeForStat[subtype], {})
    data_sera = data['sera'].get(sSubtypeForStat[subtype], {})
    if previous_stat and previous_stat.exists():
        previous_data = json.load(lzma.LZMAFile(previous_stat, "rb"))
        previous_data_antigens = previous_data['antigens'][sSubtypeForStat[subtype]].get(lab_title(lab), {})
        previous_data_sera_unique = previous_data['sera_unique'].get(sSubtypeForStat[subtype], {})
        previous_data_sera = previous_data['sera'].get(sSubtypeForStat[subtype], {})
    else:
        previous_data_antigens, previous_data_sera_unique, previous_data_sera = {}, {}, {}
    previous_sum: dict[str, int] = collections.defaultdict(int)

    heading = ' & '.join('\\ContinentHeading{{{}}}'.format(n) for n in (sHeader[nn] for nn in sContinents))
    heading = heading.replace('{TOTAL}', 'Total{TOTAL}').replace('{Sr Unique}', 'Last{Sr Unique}').replace('{Sr Uniq}', 'Last{Sr Uniq}')
    result = [
        r"\begin{WhoccStatisticsTable}  \hline",
        r"\PeriodHeading{Month} & " + heading + r"\\",
        r"\hline",
    ]
    for date in time_series.range_begin():
        stat_date = date.strftime("%Y%m")
        result.append(_statistics_table_make_line(stat_date, data_antigens=data_antigens.get(stat_date, {}), data_sera=data_sera.get(stat_date, {}), data_sera_unique=data_sera_unique.get(stat_date, {}), previous_data_antigens=previous_data_antigens.get(stat_date, {}), previous_data_sera=previous_data_sera.get(stat_date, {}).get('all', 0), previous_data_sera_unique=previous_data_sera_unique.get(stat_date, {}).get('all', 0)))
        if previous_data_antigens:
            for continent in sContinents[:-2]:
                previous_sum[continent] += previous_data_antigens.get(stat_date, {}).get(continent, 0)
            previous_sum['sera'] += previous_data_sera.get(stat_date, {}).get('all', 0)
            previous_sum['sera_unique'] += previous_data_sera_unique.get(stat_date, {}).get('all', 0)
    result.extend([
        r"\hline",
        _statistics_table_make_line("all", data_antigens=data_antigens.get('all', {}), data_sera=data_sera.get('all', {}), data_sera_unique=data_sera_unique.get('all', {}), previous_data_antigens=previous_sum, previous_data_sera=previous_sum.get('sera'), previous_data_sera_unique=previous_sum.get('sera_unique')),
        r"\hline",
        r"\end{WhoccStatisticsTable}",
    ])
    return result

def _statistics_table_make_line(date: str, data_antigens: dict, data_sera: dict, data_sera_unique: dict, previous_data_antigens: dict, previous_data_sera: int | None, previous_data_sera_unique: int | None) -> str:

    def diff_current_previous(continent):
        diff = data_antigens.get(continent, 0) - previous_data_antigens.get(continent, 0)
        if diff < 0:
            print(f"> {date} {continent}: Current: {data_antigens.get(continent, 0)} Previous: {previous_data_antigens.get(continent, 0)}", file=sys.stderr)
            diff = 0
        return diff

    def format_date():
        if date[0] == '9':
            result = 'Unknown'
        elif date == 'all':
            result = '\\color{WhoccStatisticsTableTotal} TOTAL'
        elif len(date) == 4 or date[4:] == '99':
            if time_series.period == 'month':
                result = '{}-??'.format(date[:4])
            else:
                result = '{}'.format(date[:4])
        else:
            result = '{}-{}'.format(date[:4], date[4:])
        return result

    data = [format_date()]
    if date == 'all':
        total = "Total"
    else:
        total = ""
    if previous_data_antigens:
        data.extend([r'\WhoccStatisticsTableCellTwo{}{{{}}}{{{}}}'.format(total, data_antigens.get(continent, 0), diff_current_previous(continent)) for continent in sContinents[:-3]])
        data.append( r'\WhoccStatisticsTableCellTwoTotal{{{}}}{{{}}}'.format(data_antigens.get('all', 0), diff_current_previous('all')))
        data.append( r'\WhoccStatisticsTableCellTwo{}{{{}}}{{{}}}'.format(total, data_sera.get('all', 0), data_sera.get('all', 0) - previous_data_sera))
        data.append( r'\WhoccStatisticsTableCellTwo{}{{{}}}{{{}}}'.format(total, data_sera_unique.get('all', 0), data_sera_unique.get('all', 0) - previous_data_sera_unique))
    else:
        data.extend([r'\WhoccStatisticsTableCellOne{}{{{}}}'.format(total, data_antigens.get(continent, 0)) for continent in sContinents[:-3]])
        data.append(r'\WhoccStatisticsTableCellOneTotal{{{}}}'.format(data_antigens.get(sContinents[-3], 0)))
        data.append( r'\WhoccStatisticsTableCellOne{}{{{}}}'.format(total, data_sera.get('all', 0)))
        data.append( r'\WhoccStatisticsTableCellOne{}{{{}}}'.format(total, data_sera_unique.get('all', 0)))
    return '  ' + ' & '.join(data) + ' \\\\'

def whole_page_image(images: list[Path]|Path, fit: bool = False) -> list[str]:
    if isinstance(images, list):
        if all(fn.exists() for fn in images):
            fns = ",".join(str(fn.resolve()) for fn in images)
            return [f"\\WholePagePdfMerge{{{fns}}}"]
        else:
            return list(itertools.chain.from_iterable(whole_page_image(fn, fit=fit) for fn in images))
    elif images.exists():
        if fit:
            return [f"\\WholePagePdfFit{{{images.resolve()}}}"]
        else:
            return [f"\\WholePagePdf{{{images.resolve()}}}"]
    else:
        return [f"\\newpage \\vspace*{{15em}} {{\\fontsize{{40}}{{50}} \\selectfont \\noindent \\rotatebox{{315}}{{ \\textbf{{ \\textcolor{{red}}{{{images}}} }} }}}}"]

